"""
Operator logika digunakan untuk menggabungkan dua 
atau lebih kondisi. operator logika di python

and: True jika kedua kondisi True
or: True jika salah satu kondisi True
not: Membalikan nilai boolean
"""



# contoh pengunaan
umur = 25
lulus = True
print(umur > 18 and lulus)  # True
print(umur < 18 or lulus)   # True
print(not lulus)            # False
